public class DataTypes {
    public static void main(String arg[]){
        int a = 2;
        double b = 2.0;
        float c = 2F;
        byte d = 2;
        short e = 2;
        long f = 2L;
        char g = 'f';
        boolean h = true;

        System.out.println("int" + " " + a + "\ndouble" + " " + b +
                           "\nfloat" + " " + c + "\nbyte" + " " + d +
                           "\nshort" + " " + e + "\nlong" + " " + f +
                           "\nchar" + " " + g + "\nboolean" + " " + h);


    }
}
